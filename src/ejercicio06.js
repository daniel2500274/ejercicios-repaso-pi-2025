/*
6. Área de un rectángulo
Tema: operadores aritméticos
let base = 5
let altura = 10
Calcula el área del rectángulo
*/
// Creamos una función para hacer los cálculos
function areaRectangulo(base, altura) {
        return (base * altura);
}
console.log(`El area para un rectangulo base 5 y altura 10 es ${areaRectangulo(5,10)}`)